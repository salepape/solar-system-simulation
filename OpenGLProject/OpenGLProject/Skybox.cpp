#include "Skybox.h"



Skybox::Skybox(const char* path)
{
	texture = new Texture(path, "", GL_TEXTURE_CUBE_MAP, ObjectType::SKYBOX);
	texture->LoadCubemapDDS();

	Compute();
	Store();
};

