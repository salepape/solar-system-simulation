void Skybox::Store()
{
	const unsigned int sizeofVertices = sizeof(float) * vertCoor.size();

	vao = new VertexArray();
	VertexBuffer vbo(&vertCoor[0], sizeofVertices);

	VertexBufferLayout vbl;
	vbl.Push<float>(3);
	vao->AddBuffer(vbo, vbl);

	vbo.Unbind();
	vao->Unbind();
}

